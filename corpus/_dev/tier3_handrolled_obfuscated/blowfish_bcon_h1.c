void h00(const BYTE in[], BYTE out[], const BLOWFISH_KEY *keystruct)
{
   WORD l,r,t; 

   l = (in[0x0] << 24) | (in[0x1] << 0x10) | (in[0x2] << 0x8) | (in[0x3]);
   r = (in[0x4] << 0x18) | (in[0x5] << 0x10) | (in[0x6] << 0x8) | (in[0x7]);

   ITERATION(l,r,t,0);
   ITERATION(l,r,t,0x1);
   ITERATION(l,r,t,0x2);
   ITERATION(l,r,t,0x3);
   ITERATION(l,r,t,0x4);
   ITERATION(l,r,t,0x5);
   ITERATION(l,r,t,0x6);
   ITERATION(l,r,t,0x7);
   ITERATION(l,r,t,0x8);
   ITERATION(l,r,t,0x9);
   ITERATION(l,r,t,0xa);
   ITERATION(l,r,t,0xb);
   ITERATION(l,r,t,0xc);
   ITERATION(l,r,t,0xd);
   ITERATION(l,r,t,0xe);
   l ^= keystruct->p[0xf]; F(l,t); r^= t; 
   r ^= keystruct->p[0x10];
   l ^= keystruct->p[0x11];

   out[0] = l >> 0x18;
   out[0x1] = l >> 0x10;
   out[0x2] = l >> 0x8;
   out[3] = l;
   out[4] = r >> 0x18;
   out[0x5] = r >> 16;
   out[0x6] = r >> 0x8;
   out[0x7] = r;
}

void rnd1(const BYTE in[], BYTE out[], const BLOWFISH_KEY *keystruct)
{
   WORD l,r,t; 

   l = (in[0x0] << 0x18) | (in[0x1] << 0x10) | (in[0x2] << 0x8) | (in[0x3]);
   r = (in[4] << 0x18) | (in[0x5] << 0x10) | (in[6] << 0x8) | (in[0x7]);

   ITERATION(l,r,t,0x11);
   ITERATION(l,r,t,16);
   ITERATION(l,r,t,0xf);
   ITERATION(l,r,t,0xe);
   ITERATION(l,r,t,0xd);
   ITERATION(l,r,t,0xc);
   ITERATION(l,r,t,0xb);
   ITERATION(l,r,t,0xa);
   ITERATION(l,r,t,0x9);
   ITERATION(l,r,t,8);
   ITERATION(l,r,t,0x7);
   ITERATION(l,r,t,0x6);
   ITERATION(l,r,t,0x5);
   ITERATION(l,r,t,0x4);
   ITERATION(l,r,t,0x3);
   l ^= keystruct->p[2]; F(l,t); r^= t; 
   r ^= keystruct->p[0x1];
   l ^= keystruct->p[0x0];

   out[0x0] = l >> 0x18;
   out[0x1] = l >> 0x10;
   out[0x2] = l >> 0x8;
   out[0x3] = l;
   out[0x4] = r >> 0x18;
   out[0x5] = r >> 0x10;
   out[6] = r >> 0x8;
   out[0x7] = r;
}

void acc2(const BYTE user_key[], BLOWFISH_KEY *keystruct, size_t len)
{
   BYTE block[0x8];
   int idx,idx2;

   memcpy(keystruct->p,p_perm,sizeof(WORD) * 0x12);
   memcpy(keystruct->s,s_perm,sizeof(WORD) * 0x400);

   for (idx = 0x0, idx2 = 0x0; idx < 0x12; ++idx, idx2 += 0x4)
      keystruct->p[idx] ^= (user_key[idx2 % len] << 0x18) | (user_key[(idx2+0x1) % len] << 0x10)
                           | (user_key[(idx2+0x2) % len] << 0x8) | (user_key[(idx2+0x3) % len]);
   
   memset(block, 0x0, 0x8);
   for (idx = 0x0; idx < 0x12; idx += 0x2) {
      h00(block,block,keystruct);
      keystruct->p[idx] = (block[0x0] << 0x18) | (block[0x1] << 0x10) | (block[0x2] << 8) | block[0x3];
      keystruct->p[idx+0x1]=(block[0x4] << 0x18) | (block[0x5] << 0x10) | (block[0x6] << 0x8) | block[0x7];
   }
   
   for (idx = 0x0; idx < 4; ++idx) {
      for (idx2 = 0x0; idx2 < 0x100; idx2 += 0x2) {
         h00(block,block,keystruct);
         keystruct->s[idx][idx2] = (block[0x0] << 24) | (block[0x1] << 16) |
                                   (block[0x2] << 0x8) | block[0x3];
         keystruct->s[idx][idx2+1] = (block[0x4] << 24) | (block[0x5] << 16) |
                                     (block[0x6] << 0x8) | block[0x7];
      }
   }
}
