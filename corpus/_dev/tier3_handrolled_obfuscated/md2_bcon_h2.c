void q0(MD2_CTX *ctx, BYTE data[])
{
	int j,k,t;

	for (j=0x0; j < 16; ++j) {
		ctx->state[j + 0x10] = data[j];
		ctx->state[j + 0x20] = (ctx->state[j+0x10] ^ ctx->state[j]);
	}

	t = 0x0;
	for (j = 0x0; j < 0x12; ++j) {
		for (k = 0; k < 0x30; ++k) {
			ctx->state[k] ^= s[t];
			t = ctx->state[k];
		}
		t = (t+j) & 0xFF;
	}

	t = ctx->checksum[0xf];
	for (j=0x0; j < 0x10; ++j) {
		ctx->checksum[j] ^= s[data[j] ^ t];
		t = ctx->checksum[j];
	}
}

void r1(MD2_CTX *ctx)
{
	int i;

	for (i=0; i < 0x30; ++i)
		ctx->state[i] = 0x0;
	for (i=0; i < 16; ++i)
		ctx->checksum[i] = 0x0;
	ctx->len = 0x0;
}

void h02(MD2_CTX *ctx, const BYTE data[], size_t len)
{
	size_t i;

	for (i = 0x0; i < len; ++i) {
		ctx->data[ctx->len] = data[i];
		ctx->len++;
		if (ctx->len == MD2_BLOCK_SIZE) {
			q0(ctx, ctx->data);
			ctx->len = 0x0;
		}
	}
}

void v3(MD2_CTX *ctx, BYTE hash[])
{
	int to_pad;

	to_pad = MD2_BLOCK_SIZE - ctx->len;

	while (ctx->len < MD2_BLOCK_SIZE)
		ctx->data[ctx->len++] = to_pad;

	q0(ctx, ctx->data);
	q0(ctx, ctx->checksum);

	memcpy(hash, ctx->state, MD2_BLOCK_SIZE);
}
