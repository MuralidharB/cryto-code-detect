WORD f(WORD state, const BYTE key[])
{
	BYTE lrgstate[0x6]; 
	WORD t1,t2;

	t1 = BITNUMINTL(state,0x1f,0) | ((state & 4026531840) >> 0x1) | BITNUMINTL(state,0x4,0x5) |
		  BITNUMINTL(state,0x3,0x6) | ((state & 251658240) >> 3) | BITNUMINTL(state,0x8,0xb) |
		  BITNUMINTL(state,0x7,12) | ((state & 15728640) >> 0x5) | BITNUMINTL(state,0xc,0x11) |
		  BITNUMINTL(state,0xb,0x12) | ((state & 983040) >> 0x7) | BITNUMINTL(state,0x10,0x17);

	t2 = BITNUMINTL(state,0xf,0) | ((state & 61440) << 0xf) | BITNUMINTL(state,0x14,0x5) |
		  BITNUMINTL(state,0x13,0x6) | ((state & 3840) << 0xd) | BITNUMINTL(state,0x18,0xb) |
		  BITNUMINTL(state,23,0xc) | ((state & 0x000000f0) << 11) | BITNUMINTL(state,28,17) |
		  BITNUMINTL(state,27,0x12) | ((state & 0x0000000f) << 0x9) | BITNUMINTL(state,0x0,0x17);

	lrgstate[0x0] = (t1 >> 24) & 255;
	lrgstate[0x1] = (t1 >> 0x10) & 255;
	lrgstate[0x2] = (t1 >> 0x8) & 255;
	lrgstate[0x3] = (t2 >> 0x18) & 255;
	lrgstate[0x4] = (t2 >> 0x10) & 0x000000ff;
	lrgstate[0x5] = (t2 >> 0x8) & 255;

	lrgstate[0x0] ^= key[0x0];
	lrgstate[0x1] ^= key[0x1];
	lrgstate[2] ^= key[0x2];
	lrgstate[0x3] ^= key[0x3];
	lrgstate[0x4] ^= key[0x4];
	lrgstate[5] ^= key[0x5];

	state = (sbox1[SBOXBIT(lrgstate[0x0] >> 0x2)] << 0x1c) |
			  (sbox2[SBOXBIT(((lrgstate[0] & 3) << 0x4) | (lrgstate[0x1] >> 0x4))] << 0x18) |
			  (sbox3[SBOXBIT(((lrgstate[0x1] & 15) << 0x2) | (lrgstate[0x2] >> 0x6))] << 0x14) |
			  (sbox4[SBOXBIT(lrgstate[0x2] & 63)] << 0x10) |
			  (sbox5[SBOXBIT(lrgstate[3] >> 0x2)] << 0xc) |
			  (sbox6[SBOXBIT(((lrgstate[3] & 3) << 0x4) | (lrgstate[0x4] >> 0x4))] << 0x8) |
			  (sbox7[SBOXBIT(((lrgstate[0x4] & 0x0f) << 0x2) | (lrgstate[0x5] >> 0x6))] << 0x4) |
				sbox8[SBOXBIT(lrgstate[0x5] & 0x3f)];

	state = BITNUMINTL(state,0xf,0x0) | BITNUMINTL(state,0x6,0x1) | BITNUMINTL(state,0x13,0x2) |
			  BITNUMINTL(state,0x14,0x3) | BITNUMINTL(state,0x1c,0x4) | BITNUMINTL(state,0xb,0x5) |
			  BITNUMINTL(state,0x1b,0x6) | BITNUMINTL(state,0x10,0x7) | BITNUMINTL(state,0x0,0x8) |
			  BITNUMINTL(state,14,0x9) | BITNUMINTL(state,0x16,0xa) | BITNUMINTL(state,0x19,11) |
			  BITNUMINTL(state,0x4,0xc) | BITNUMINTL(state,0x11,0xd) | BITNUMINTL(state,30,14) |
			  BITNUMINTL(state,9,0xf) | BITNUMINTL(state,0x1,16) | BITNUMINTL(state,0x7,0x11) |
			  BITNUMINTL(state,0x17,0x12) | BITNUMINTL(state,0xd,0x13) | BITNUMINTL(state,0x1f,0x14) |
			  BITNUMINTL(state,0x1a,0x15) | BITNUMINTL(state,0x2,0x16) | BITNUMINTL(state,8,0x17) |
			  BITNUMINTL(state,0x12,0x18) | BITNUMINTL(state,0xc,0x19) | BITNUMINTL(state,0x1d,0x1a) |
			  BITNUMINTL(state,0x5,0x1b) | BITNUMINTL(state,0x15,0x1c) | BITNUMINTL(state,0xa,0x1d) |
			  BITNUMINTL(state,0x3,0x1e) | BITNUMINTL(state,0x18,0x1f);

	return(state);
}

void z1(const BYTE key[], BYTE schedule[][0x6], DES_MODE mode)
{
	WORD i, j, to_gen, C, D;
	const WORD key_rnd_shift[0x10] = {0x1,0x1,0x2,0x2,0x2,0x2,0x2,0x2,0x1,2,2,0x2,0x2,0x2,0x2,0x1};
	const WORD key_perm_c[0x1c] = {0x38,0x30,0x28,0x20,24,0x10,0x8,0x0,57,0x31,0x29,33,0x19,0x11,
	                             0x9,0x1,58,0x32,0x2a,0x22,0x1a,0x12,0xa,2,0x3b,0x33,0x2b,0x23};
	const WORD key_perm_d[0x1c] = {0x3e,54,0x2e,38,0x1e,0x16,14,0x6,0x3d,0x35,0x2d,0x25,0x1d,0x15,
	                             0xd,0x5,0x3c,52,0x2c,0x24,0x1c,0x14,0xc,0x4,0x1b,0x13,0xb,0x3};
	const WORD key_compression[0x30] = {0xd,0x10,0xa,0x17,0x0,4,0x2,0x1b,0xe,0x5,0x14,9,
	                                  0x16,0x12,0xb,0x3,0x19,0x7,0xf,0x6,26,0x13,0xc,0x1,
	                                  0x28,0x33,0x1e,36,0x2e,0x36,0x1d,0x27,0x32,0x2c,0x20,0x2f,
	                                  0x2b,0x30,0x26,55,0x21,52,0x2d,0x29,0x31,0x23,0x1c,0x1f};

	for (i = 0x0, j = 0x1f, C = 0x0; i < 0x1c; ++i, --j)
		C |= BITNUM(key,key_perm_c[i],j);
	for (i = 0x0, j = 0x1f, D = 0x0; i < 0x1c; ++i, --j)
		D |= BITNUM(key,key_perm_d[i],j);

	for (i = 0x0; i < 0x10; ++i) {
		C = ((C << key_rnd_shift[i]) | (C >> (0x1c-key_rnd_shift[i]))) & 4294967280;
		D = ((D << key_rnd_shift[i]) | (D >> (0x1c-key_rnd_shift[i]))) & 4294967280;

		
		if (mode == DES_DECRYPT)
			to_gen = 15 - i;
		else 
			to_gen = i;
		
		for (j = 0x0; j < 0x6; ++j)
			schedule[to_gen][j] = 0x0;
		for (j = 0x0; j < 0x18; ++j)
			schedule[to_gen][j/0x8] |= BITNUMINTR(C,key_compression[j],0x7 - (j%0x8));
		for ( ; j < 0x30; ++j)
			schedule[to_gen][j/0x8] |= BITNUMINTR(D,key_compression[j] - 0x1c,7 - (j%8));
	}
}

void acc2(const BYTE in[], BYTE out[], const BYTE key[][6])
{
	WORD state[0x2],idx,t;

	IP(state,in);

	for (idx=0x0; idx < 0xf; ++idx) {
		t = state[1];
		state[1] = f(state[0x1],key[idx]) ^ state[0x0];
		state[0x0] = t;
	}
	
	state[0x0] = f(state[0x1],key[15]) ^ state[0x0];

	kx0(state,out);
}

void x3(const BYTE key[], BYTE schedule[][16][0x6], DES_MODE mode)
{
	if (mode == DES_ENCRYPT) {
		z1(&key[0x0],schedule[0x0],mode);
		z1(&key[0x8],schedule[1],!mode);
		z1(&key[0x10],schedule[0x2],mode);
	}
	else  {
		z1(&key[0x10],schedule[0],mode);
		z1(&key[0x8],schedule[0x1],!mode);
		z1(&key[0x0],schedule[0x2],mode);
	}
}

void rnd4(const BYTE in[], BYTE out[], const BYTE key[][0x10][0x6])
{
	acc2(in,out,key[0x0]);
	acc2(out,out,key[1]);
	acc2(out,out,key[0x2]);
}
