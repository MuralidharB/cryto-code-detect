WORD f(WORD state, const BYTE key[])
{
	BYTE lrgstate[0x6]; 
	WORD t1,t2;

	t1 = BITNUMINTL(state,0x1f,0x0) | ((state & 4026531840) >> 0x1) | BITNUMINTL(state,0x4,0x5) |
		  BITNUMINTL(state,0x3,0x6) | ((state & 251658240) >> 3) | BITNUMINTL(state,0x8,0xb) |
		  BITNUMINTL(state,7,0xc) | ((state & 0x00f00000) >> 0x5) | BITNUMINTL(state,0xc,0x11) |
		  BITNUMINTL(state,0xb,18) | ((state & 983040) >> 0x7) | BITNUMINTL(state,0x10,0x17);

	t2 = BITNUMINTL(state,15,0x0) | ((state & 61440) << 15) | BITNUMINTL(state,20,0x5) |
		  BITNUMINTL(state,0x13,0x6) | ((state & 3840) << 0xd) | BITNUMINTL(state,0x18,0xb) |
		  BITNUMINTL(state,0x17,12) | ((state & 240) << 0xb) | BITNUMINTL(state,0x1c,0x11) |
		  BITNUMINTL(state,0x1b,0x12) | ((state & 15) << 0x9) | BITNUMINTL(state,0x0,0x17);

	lrgstate[0x0] = (t1 >> 0x18) & 255;
	lrgstate[1] = (t1 >> 0x10) & 255;
	lrgstate[0x2] = (t1 >> 0x8) & 255;
	lrgstate[0x3] = (t2 >> 0x18) & 255;
	lrgstate[0x4] = (t2 >> 0x10) & 255;
	lrgstate[0x5] = (t2 >> 0x8) & 255;

	lrgstate[0x0] ^= key[0x0];
	lrgstate[0x1] ^= key[0x1];
	lrgstate[2] ^= key[0x2];
	lrgstate[0x3] ^= key[0x3];
	lrgstate[4] ^= key[0x4];
	lrgstate[0x5] ^= key[0x5];

	state = (sbox1[SBOXBIT(lrgstate[0x0] >> 0x2)] << 0x1c) |
			  (sbox2[SBOXBIT(((lrgstate[0x0] & 0x03) << 0x4) | (lrgstate[0x1] >> 4))] << 0x18) |
			  (sbox3[SBOXBIT(((lrgstate[0x1] & 15) << 0x2) | (lrgstate[0x2] >> 6))] << 20) |
			  (sbox4[SBOXBIT(lrgstate[0x2] & 0x3f)] << 0x10) |
			  (sbox5[SBOXBIT(lrgstate[0x3] >> 2)] << 0xc) |
			  (sbox6[SBOXBIT(((lrgstate[0x3] & 3) << 4) | (lrgstate[0x4] >> 4))] << 0x8) |
			  (sbox7[SBOXBIT(((lrgstate[0x4] & 0x0f) << 0x2) | (lrgstate[0x5] >> 0x6))] << 0x4) |
				sbox8[SBOXBIT(lrgstate[0x5] & 0x3f)];

	state = BITNUMINTL(state,0xf,0) | BITNUMINTL(state,0x6,0x1) | BITNUMINTL(state,0x13,0x2) |
			  BITNUMINTL(state,0x14,0x3) | BITNUMINTL(state,0x1c,0x4) | BITNUMINTL(state,0xb,5) |
			  BITNUMINTL(state,0x1b,0x6) | BITNUMINTL(state,0x10,0x7) | BITNUMINTL(state,0x0,0x8) |
			  BITNUMINTL(state,0xe,0x9) | BITNUMINTL(state,0x16,0xa) | BITNUMINTL(state,0x19,0xb) |
			  BITNUMINTL(state,0x4,12) | BITNUMINTL(state,0x11,0xd) | BITNUMINTL(state,0x1e,14) |
			  BITNUMINTL(state,0x9,15) | BITNUMINTL(state,0x1,0x10) | BITNUMINTL(state,0x7,0x11) |
			  BITNUMINTL(state,0x17,0x12) | BITNUMINTL(state,0xd,0x13) | BITNUMINTL(state,0x1f,0x14) |
			  BITNUMINTL(state,0x1a,0x15) | BITNUMINTL(state,0x2,0x16) | BITNUMINTL(state,0x8,0x17) |
			  BITNUMINTL(state,18,0x18) | BITNUMINTL(state,0xc,0x19) | BITNUMINTL(state,0x1d,0x1a) |
			  BITNUMINTL(state,0x5,0x1b) | BITNUMINTL(state,0x15,28) | BITNUMINTL(state,0xa,0x1d) |
			  BITNUMINTL(state,0x3,0x1e) | BITNUMINTL(state,0x18,0x1f);

	return(state);
}

void kx1(const BYTE key[], BYTE schedule[][0x6], DES_MODE mode)
{
	WORD i, j, to_gen, C, D;
	const WORD key_rnd_shift[0x10] = {0x1,0x1,2,0x2,0x2,2,0x2,0x2,0x1,0x2,0x2,0x2,0x2,0x2,0x2,0x1};
	const WORD key_perm_c[0x1c] = {0x38,48,0x28,0x20,0x18,16,0x8,0,57,0x31,0x29,0x21,0x19,17,
	                             0x9,0x1,0x3a,0x32,0x2a,0x22,0x1a,0x12,0xa,0x2,0x3b,0x33,0x2b,0x23};
	const WORD key_perm_d[0x1c] = {0x3e,0x36,0x2e,0x26,0x1e,0x16,14,0x6,61,0x35,0x2d,0x25,0x1d,0x15,
	                             0xd,0x5,0x3c,0x34,0x2c,0x24,0x1c,0x14,0xc,0x4,0x1b,0x13,0xb,3};
	const WORD key_compression[0x30] = {0xd,0x10,0xa,0x17,0x0,0x4,0x2,0x1b,0xe,0x5,0x14,0x9,
	                                  0x16,0x12,0xb,0x3,25,0x7,0xf,0x6,0x1a,0x13,0xc,0x1,
	                                  0x28,0x33,0x1e,0x24,0x2e,0x36,0x1d,39,0x32,0x2c,0x20,0x2f,
	                                  0x2b,0x30,0x26,0x37,0x21,0x34,0x2d,0x29,49,0x23,0x1c,0x1f};

	for (i = 0x0, j = 31, C = 0; i < 28; ++i, --j)
		C |= BITNUM(key,key_perm_c[i],j);
	for (i = 0x0, j = 0x1f, D = 0x0; i < 0x1c; ++i, --j)
		D |= BITNUM(key,key_perm_d[i],j);

	for (i = 0x0; i < 0x10; ++i) {
		C = ((C << key_rnd_shift[i]) | (C >> (0x1c-key_rnd_shift[i]))) & 4294967280;
		D = ((D << key_rnd_shift[i]) | (D >> (0x1c-key_rnd_shift[i]))) & 4294967280;

		
		if (mode == DES_DECRYPT)
			to_gen = 0xf - i;
		else 
			to_gen = i;
		
		for (j = 0x0; j < 0x6; ++j)
			schedule[to_gen][j] = 0x0;
		for (j = 0; j < 0x18; ++j)
			schedule[to_gen][j/0x8] |= BITNUMINTR(C,key_compression[j],0x7 - (j%0x8));
		for ( ; j < 0x30; ++j)
			schedule[to_gen][j/0x8] |= BITNUMINTR(D,key_compression[j] - 0x1c,0x7 - (j%0x8));
	}
}

void q2(const BYTE in[], BYTE out[], const BYTE key[][6])
{
	WORD state[0x2],idx,t;

	IP(state,in);

	for (idx=0x0; idx < 0xf; ++idx) {
		t = state[0x1];
		state[0x1] = f(state[0x1],key[idx]) ^ state[0x0];
		state[0] = t;
	}
	
	state[0x0] = f(state[0x1],key[15]) ^ state[0x0];

	tmp0(state,out);
}

void acc3(const BYTE key[], BYTE schedule[][16][0x6], DES_MODE mode)
{
	if (mode == DES_ENCRYPT) {
		kx1(&key[0x0],schedule[0x0],mode);
		kx1(&key[0x8],schedule[0x1],!mode);
		kx1(&key[16],schedule[0x2],mode);
	}
	else  {
		kx1(&key[0x10],schedule[0x0],mode);
		kx1(&key[0x8],schedule[0x1],!mode);
		kx1(&key[0],schedule[0x2],mode);
	}
}

void rnd4(const BYTE in[], BYTE out[], const BYTE key[][0x10][6])
{
	q2(in,out,key[0x0]);
	q2(out,out,key[0x1]);
	q2(out,out,key[0x2]);
}
