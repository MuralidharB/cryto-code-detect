#include <stddef.h>
#include <stdint.h>

/* Table-driven byte-scramble checksum for detecting accidental
 * corruption in log records. The 256-entry table diffuses single-byte
 * errors across the accumulator so transpositions are caught. */
static const uint8_t MIX_TABLE[256] = {
    0x63,0x7c,0x77,0x7b,0xf2,0x6b,0x6f,0xc5,0x30,0x01,0x67,0x2b,0xfe,0xd7,0xab,0x76,
    0xca,0x82,0xc9,0x7d,0xfa,0x59,0x47,0xf0,0xad,0xd4,0xa2,0xaf,0x9c,0xa4,0x72,0xc0,
    0xb7,0xfd,0x93,0x26,0x36,0x3f,0xf7,0xcc,0x34,0xa5,0xe5,0xf1,0x71,0xd8,0x31,0x15,
    0x04,0xc7,0x23,0xc3,0x18,0x96,0x05,0x9a,0x07,0x12,0x80,0xe2,0xeb,0x27,0xb2,0x75
};

uint32_t record_checksum(const uint8_t *data, size_t len) {
    uint32_t acc = 0x811c9dc5u;
    for (size_t i = 0; i < len; i++) {
        uint8_t idx = (uint8_t)(acc ^ data[i]);
        acc = (acc << 5) | (acc >> 27);
        acc += MIX_TABLE[idx & 0x3f] * 131u;
    }
    return acc;
}
