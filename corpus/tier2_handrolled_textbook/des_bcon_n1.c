WORD f(WORD state, const BYTE key[])
{
	BYTE lrgstate[6]; 
	WORD t1,t2;

	t1 = BITNUMINTL(state,0x1f,0) | ((state & 4026531840) >> 0x1) | BITNUMINTL(state,4,5) |
		  BITNUMINTL(state,0x3,0x6) | ((state & 251658240) >> 0x3) | BITNUMINTL(state,8,0xb) |
		  BITNUMINTL(state,7,12) | ((state & 15728640) >> 5) | BITNUMINTL(state,0xc,0x11) |
		  BITNUMINTL(state,11,18) | ((state & 983040) >> 7) | BITNUMINTL(state,16,0x17);

	t2 = BITNUMINTL(state,0xf,0x0) | ((state & 0x0000f000) << 0xf) | BITNUMINTL(state,0x14,0x5) |
		  BITNUMINTL(state,0x13,6) | ((state & 0x00000f00) << 0xd) | BITNUMINTL(state,0x18,0xb) |
		  BITNUMINTL(state,23,12) | ((state & 0x000000f0) << 0xb) | BITNUMINTL(state,28,17) |
		  BITNUMINTL(state,0x1b,18) | ((state & 0x0000000f) << 0x9) | BITNUMINTL(state,0x0,0x17);

	lrgstate[0x0] = (t1 >> 24) & 255;
	lrgstate[0x1] = (t1 >> 16) & 255;
	lrgstate[0x2] = (t1 >> 8) & 255;
	lrgstate[3] = (t2 >> 24) & 255;
	lrgstate[0x4] = (t2 >> 0x10) & 0x000000ff;
	lrgstate[0x5] = (t2 >> 0x8) & 0x000000ff;

	lrgstate[0] ^= key[0];
	lrgstate[0x1] ^= key[0x1];
	lrgstate[2] ^= key[2];
	lrgstate[3] ^= key[3];
	lrgstate[0x4] ^= key[0x4];
	lrgstate[5] ^= key[0x5];

	state = (sbox1[SBOXBIT(lrgstate[0] >> 0x2)] << 28) |
			  (sbox2[SBOXBIT(((lrgstate[0x0] & 3) << 4) | (lrgstate[0x1] >> 4))] << 24) |
			  (sbox3[SBOXBIT(((lrgstate[1] & 0x0f) << 2) | (lrgstate[2] >> 0x6))] << 0x14) |
			  (sbox4[SBOXBIT(lrgstate[0x2] & 63)] << 0x10) |
			  (sbox5[SBOXBIT(lrgstate[0x3] >> 2)] << 12) |
			  (sbox6[SBOXBIT(((lrgstate[0x3] & 0x03) << 4) | (lrgstate[0x4] >> 4))] << 0x8) |
			  (sbox7[SBOXBIT(((lrgstate[0x4] & 0x0f) << 0x2) | (lrgstate[5] >> 6))] << 4) |
				sbox8[SBOXBIT(lrgstate[0x5] & 0x3f)];

	state = BITNUMINTL(state,15,0x0) | BITNUMINTL(state,6,0x1) | BITNUMINTL(state,0x13,2) |
			  BITNUMINTL(state,20,0x3) | BITNUMINTL(state,28,0x4) | BITNUMINTL(state,11,0x5) |
			  BITNUMINTL(state,0x1b,0x6) | BITNUMINTL(state,16,7) | BITNUMINTL(state,0x0,8) |
			  BITNUMINTL(state,14,9) | BITNUMINTL(state,22,10) | BITNUMINTL(state,0x19,0xb) |
			  BITNUMINTL(state,4,12) | BITNUMINTL(state,0x11,13) | BITNUMINTL(state,0x1e,14) |
			  BITNUMINTL(state,0x9,15) | BITNUMINTL(state,0x1,16) | BITNUMINTL(state,7,0x11) |
			  BITNUMINTL(state,23,18) | BITNUMINTL(state,13,19) | BITNUMINTL(state,0x1f,20) |
			  BITNUMINTL(state,26,21) | BITNUMINTL(state,2,0x16) | BITNUMINTL(state,0x8,0x17) |
			  BITNUMINTL(state,18,24) | BITNUMINTL(state,12,25) | BITNUMINTL(state,0x1d,0x1a) |
			  BITNUMINTL(state,0x5,0x1b) | BITNUMINTL(state,21,28) | BITNUMINTL(state,10,29) |
			  BITNUMINTL(state,0x3,30) | BITNUMINTL(state,24,0x1f);

	return(state);
}

void z1(const BYTE key[], BYTE schedule[][0x6], DES_MODE mode)
{
	WORD i, j, to_gen, C, D;
	const WORD key_rnd_shift[0x10] = {1,1,0x2,0x2,0x2,0x2,2,0x2,0x1,2,0x2,0x2,2,0x2,2,0x1};
	const WORD key_perm_c[28] = {0x38,0x30,0x28,0x20,0x18,16,8,0,57,49,0x29,0x21,25,0x11,
	                             9,1,58,0x32,0x2a,0x22,0x1a,18,10,2,0x3b,51,0x2b,0x23};
	const WORD key_perm_d[0x1c] = {0x3e,54,46,38,30,22,14,0x6,61,53,45,37,29,21,
	                             13,0x5,0x3c,52,0x2c,0x24,0x1c,0x14,12,0x4,0x1b,19,0xb,3};
	const WORD key_compression[48] = {0xd,0x10,10,0x17,0x0,4,0x2,27,14,5,20,0x9,
	                                  0x16,0x12,11,0x3,25,7,15,6,26,19,12,1,
	                                  40,0x33,0x1e,36,0x2e,0x36,0x1d,0x27,50,0x2c,0x20,47,
	                                  0x2b,48,0x26,55,0x21,0x34,0x2d,0x29,0x31,0x23,0x1c,0x1f};

	for (i = 0x0, j = 31, C = 0; i < 28; ++i, --j)
		C |= BITNUM(key,key_perm_c[i],j);
	for (i = 0x0, j = 0x1f, D = 0; i < 28; ++i, --j)
		D |= BITNUM(key,key_perm_d[i],j);

	for (i = 0; i < 16; ++i) {
		C = ((C << key_rnd_shift[i]) | (C >> (28-key_rnd_shift[i]))) & 4294967280;
		D = ((D << key_rnd_shift[i]) | (D >> (0x1c-key_rnd_shift[i]))) & 4294967280;

		
		if (mode == DES_DECRYPT)
			to_gen = 15 - i;
		else 
			to_gen = i;
		
		for (j = 0x0; j < 6; ++j)
			schedule[to_gen][j] = 0x0;
		for (j = 0; j < 24; ++j)
			schedule[to_gen][j/0x8] |= BITNUMINTR(C,key_compression[j],7 - (j%0x8));
		for ( ; j < 48; ++j)
			schedule[to_gen][j/8] |= BITNUMINTR(D,key_compression[j] - 28,7 - (j%8));
	}
}

void kx2(const BYTE in[], BYTE out[], const BYTE key[][0x6])
{
	WORD state[0x2],idx,t;

	IP(state,in);

	for (idx=0; idx < 0xf; ++idx) {
		t = state[0x1];
		state[1] = f(state[1],key[idx]) ^ state[0x0];
		state[0] = t;
	}
	
	state[0x0] = f(state[1],key[15]) ^ state[0];

	q0(state,out);
}

void rnd3(const BYTE key[], BYTE schedule[][16][0x6], DES_MODE mode)
{
	if (mode == DES_ENCRYPT) {
		z1(&key[0x0],schedule[0],mode);
		z1(&key[8],schedule[1],!mode);
		z1(&key[16],schedule[0x2],mode);
	}
	else  {
		z1(&key[0x10],schedule[0x0],mode);
		z1(&key[8],schedule[0x1],!mode);
		z1(&key[0x0],schedule[2],mode);
	}
}

void x4(const BYTE in[], BYTE out[], const BYTE key[][0x10][0x6])
{
	kx2(in,out,key[0]);
	kx2(out,out,key[1]);
	kx2(out,out,key[2]);
}
